function reco( i ){
    let content = document.getElementById(i).value;

    if(infos[0]){
        infos[0].close();
    }
    map.panTo(new google.maps.LatLng(LatLng[i][0], LatLng[i][1]));
    var iwopts = {
        content: content,
        position: map.getCenter()
    }
    var info = new google.maps.InfoWindow(iwopts);
    infos[0] = info;
    info.open(map);
}
